interface Model {
  name: string
  costPerToken: number
  avgResponseTime: number
  complexityThreshold: number
}
class LLMRouter {
  private client: OpenAI
  private models: Model[]
  constructor(apiKey: string) {
    this.client = createOpenAI({
      apiKey: apiKey,
      baseURL: 'https://api.naga.ac/v1'
    })
    this.models = [
      {
        name: 'gpt-3.5-turbo-0125',
        costPerToken: 0.0001,
        avgResponseTime: 1.0,
        complexityThreshold: 0.3
      },
      {
        name: 'gpt-3.5-turbo-1106',
        costPerToken: 0.00012,
        avgResponseTime: 1.1,
        complexityThreshold: 0.35
      },
      {
        name: 'gpt-3.5-turbo-1106',
        costPerToken: 0.00025,
        avgResponseTime: 0.9,
        complexityThreshold: 0.8
      }
    ]
  }

  calculateComplexity(prompt: string): number {
    const words = prompt.split(' ')
    const totalWords = words.length
    const uniqueWords = new Set(words).size
    const avgWordLength =
      words.reduce((sum, word) => sum + word.length, 0) / totalWords

    const lexicalDiversity = uniqueWords / totalWords
    const normalizedAvgWordLength = Math.min(avgWordLength / 10, 1.0)

    return lexicalDiversity * 0.5 + normalizedAvgWordLength * 0.5
  }

  selectModel(prompt: string): Model {
    const complexity = this.calculateComplexity(prompt)
    for (const model of this.models) {
      if (complexity <= model.complexityThreshold) {
        return model
      }
    }
    return this.models[this.models.length - 1]
  }
}

async function confirmPurchase(symbol: string, price: number, amount: number) {
  'use server'

  const aiState = getMutableAIState<typeof AI>()

  const purchasing = createStreamableUI(
    <div className="inline-flex items-start gap-1 md:items-center">
      {spinner}
      <p className="mb-2">
        Purchasing {amount} ${symbol}...
      </p>
    </div>
  )

  const systemMessage = createStreamableUI(null)

  runAsyncFnWithoutBlocking(async () => {
    await sleep(1000)

    purchasing.update(
      <div className="inline-flex items-start gap-1 md:items-center">
        {spinner}
        <p className="mb-2">
          Purchasing {amount} ${symbol}... working on it...
        </p>
      </div>
    )

    await sleep(1000)

    purchasing.done(
      <div>
        <p className="mb-2">
          You have successfully purchased {amount} ${symbol}. Total cost:{' '}
          {formatNumber(amount * price)}
        </p>
      </div>
    )

    systemMessage.done(
      <SystemMessage>
        You have purchased {amount} shares of {symbol} at ${price}. Total cost ={' '}
        {formatNumber(amount * price)}.
      </SystemMessage>
    )

    aiState.done({
      ...aiState.get(),
      messages: [
        ...aiState.get().messages,
        {
          id: nanoid(),
          role: 'system',
          content: `[User has purchased ${amount} shares of ${symbol} at ${price}. Total cost = ${
            amount * price
          }]`
        }
      ]
    })
  })

  return {
    purchasingUI: purchasing.value,
    newMessage: {
      id: nanoid(),
      display: systemMessage.value
    }
  }
}

